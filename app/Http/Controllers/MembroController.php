     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $membro = Membro::find($id);
        if($membro->ativo == 0)
        {
            $membro->ativo = 1;
        }
        else
        {
            $membro->ativo = 0;
        }
        $membro->save();
        return redirect()->back();
    }

